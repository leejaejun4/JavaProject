public class Example_person {

	// 멤버변수
	String name;
	float height;
	float weight;

	public Example_person(String name) {
		this.name = name;
	}

	public Example_person(String name, int height, int weight) {
		this.name = name;
		this.height = height;
		this.weight = weight;

	}

}
