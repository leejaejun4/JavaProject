package chapter_07;

public class Guest {

	// 멤버 변수
	private String name;
	private String gender;

	// 생성자

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

}
